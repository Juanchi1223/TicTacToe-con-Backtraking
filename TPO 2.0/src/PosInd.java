public class PosInd {
    int pos;
    int indice;

    public PosInd(int p,int i){
        this.pos = p;
        this.indice = i;
    }

    public int getPos() {
        return pos;
    }

    public int getIndice() {
        return indice;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
