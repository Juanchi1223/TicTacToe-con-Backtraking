public class PosInd {
    int pos;
    int score;

    public PosInd(int p,int s){
        this.pos = p;
        this.score = s;
    }

    public int getPos() {
        return pos;
    }
    public int getScore() {
        return score;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }
}
